    <p>Yuriko Koike a également souligné que Tokyo comptait mettre en avant ses initiatives en faveur de la durabilité et de la création d'un héritage olympique pour les générations futures : « Aux côtés des villes du C40, Tokyo consacre une part importante de son énergie à trouver des solutions aux problèmes environnementaux qui nous concernent tous, et ce pour le bien de notre planète et de nos enfants. »

    <h1>Paris et Los Angeles travailleront ensemble sur la durabilité, l'inclusion et l'innovation</h1>
    <h2 class="story__main--heading">Suite à l'attribution conjointe des Jeux Olympiques d'été de 2024 et 2028 à Paris et Los Angeles respectivement, les deux villes ont choisi de renforcer leurs liens déjà étroits en coopérant sur plusieurs enjeux planétaires de premier plan.</h2>
  </div>
  <div class="story__content container">
    <p>Les maires des deux villes, Anne Hidalgo (Paris) et Eric Garcetti (Los Angeles), ont assisté ensemble à la première édition de Together4Climate les 22 et 23 octobre à Paris en présence du président du CIO, Thomas Bach. Dans la foulée, ils ont signé un protocole d'accord sur la coopération olympique.</p>
    <p>Paris et Los Angeles vont donc tenter d'approfondir leurs relations afin de travailler en étroite collaboration et de partager les bonnes pratiques en matière de durabilité, d'inclusion et d'innovation.</p>
    <div class="story__img"></div>
    <p>« Anne Hidalgo et moi-même pensons que les Jeux Olympiques sont une source de motivation et de transformation, a confié Eric Garcetti. Ces Jeux sont l'occasion de collaborer et de prouver qu'en s'attaquant aux grands défis à l'échelle locale, il est possible de trouver des solutions durables qui créent des emplois, améliorent la santé publique et contribuent à la croissance de nos économies. »</p>
    <p>Organisée par le C40 Cities Climate Leadership Group, la conférence Together4Climate a réuni des maires et des entreprises du monde entier pour débattre des moyens à mettre en œuvre pour lutter contre le changement climatique et atteindre les objectifs de l'accord de Paris.</p>
    <p>À l'issue de cet événement, Anne Hidalgo, Eric Garcetti et dix autres maires membres du comité de pilotage du C40 ont signé la déclaration sur les rues sans énergies fossiles du C40, laquelle fixe une série d'objectifs ambitieux sur des sujets comme le changement climatique, la pollution de l'air et l'amélioration de la qualité de vie.</p>
    <p>Les douze signataires de la déclaration se sont engagés, au nom de leurs villes, à atteindre l'objectif zéro émissions d'ici 2030. Tokyo, ville hôte des Jeux Olympiques de 2020, y travaille déjà.</p>
  </div>

